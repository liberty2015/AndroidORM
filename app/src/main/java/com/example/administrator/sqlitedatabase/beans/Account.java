package com.example.administrator.sqlitedatabase.beans;

import java.util.Date;

/**
 * Created by Administrator on 2016/7/29.
 */

public class Account {
    private int _id;
    private String name;
    private int gender;
    private int age;
    private Date birth;

    public int get_id() {
        return _id;
    }

    public void set_id(int _id) {
        this._id = _id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getGender() {
        return gender;
    }

    public void setGender(int gender) {
        this.gender = gender;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public Date getBirth() {
        return birth;
    }

    public void setBirth(Date birth) {
        this.birth = birth;
    }
}
